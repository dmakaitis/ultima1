;-----------------------------------------------------------
;                           start
;
; Called by hello.prg after the system has been initialized.
;-----------------------------------------------------------

start:  lda     #$00                    ; Disable screen
        sta     VIC_CTRL1

        ldx     #$20                    ; Fill memory $2000-$5FFF with $40
        stx     $FD
        lda     #$00
        tay
        sta     $FC
        ldx     #$40
@loop:  sta     ($FC),y
        iny
        bne     @loop
        inc     $FD
        dex
        bne     @loop

        lda     #$08                    ; $FE hold the number of blocks we need to copy
        sta     $FE

        lda     #$60                    ; Copy from $0C9E-$0EB5 to $4660-$4777
        sta     @store + 1                      
        lda     #$46                    ;     then $0EB6-$10CD, then $10CE-12E5
        sta     @store + 2              ;     then $12E6-$14FD, then $14FE-1715
        lda     #<origin_logo           ;     then $1716-$192D, then $192E-1B45
        sta     @load + 1               ;     then $1B46-$1D5D
        lda     #>origin_logo
        sta     @load + 2
@copy_block:
        ldx     #$02                    ; Copy $0118 bytes
        ldy     #$18
@load:  lda     $0C9E
@store: sta     $2500
        inc     @load + 1               ; Increment the source address by 1
        bne     @increment_store
        inc     @load + 2
@increment_store:
        inc     @store + 1              ; Increment the target address by 1
        bne     @advance
        inc     @store + 2
@advance:
        dey
        bne     @load
        dex
        bne     @load

        lda     @store + 1              ; Advance storage target by $28 bytes to $48A0
        clc
        adc     #$28
        sta     @store + 1
        lda     @store + 2
        adc     #$00
        sta     @store + 2

        dec     $FE
        bne     @copy_block             ; Copy 7 more times, each time advancing target start by $0240 bytes...

        ldx     #$00                    ; Fill screen memory with 0x60 (underscores)
        lda     #$60
@loop_screen:
        sta     $0400,x
        sta     $0500,x
        sta     $0600,x
        sta     $06F0,x
        inx
        bne     @loop_screen

        lda     #$00
        sta     VIC_BORDERCOLOR
        lda     #$37                    ; Enable bitmap mode
        sta     VIC_CTRL1
        lda     #$08                    ; Enable multicolor mode
        sta     VIC_CTRL2
        lda     #$18                    ; Select screen memory at $0400, bitmap at $2000
        sta     VIC_VIDEO_ADR

        jsr     L1578

        ldx     #$05                    ; Cache the 'OU' file under KERNEL ROM
        jsr     load_file

        ldx     #$0C                    ; Load the 'IN' file
        jsr     load_file_cached

        jmp     L6800




